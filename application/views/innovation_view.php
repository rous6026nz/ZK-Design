	<p class="drop_caps">
            As part of the Bachelor of Applied Science program offered by Yoobee ACG School of Design, Design Innovation presented the opportunity to potentially invent a product,
	    process or service by the identification of faults, issues and pain points of current real-world solutions.
	</p>
	<p>
	    During the innovation process, I was encouraged to choose a topic/product and research all the current options available to the consumer, and analyse the benefits and
	    disadvantages of each solution. This process allowed me to identify potential issues, problems and needs in the current aerosol actuator market, and help in the creation
	    of a new product or solution to an issue or unresolved problem.  
	</p>
	<p>
	    For this paper, I decided to research aerosol actuators for aerosol spray paints and common household cleaners. After analysing various actuators currently available on
	    the market, the disadvantages identifyied included the overwhelming amount of variety available and the minimal differences between each option, the short life cycle of
	    the actuator, the materials used to manufacture and the potential environmental impact.
	</p>
	<p>
	    From this analysis, the innovation of a Mutli-Width Aerosol Actuator was the result of my research and studies. This incremental improvement upon the conventional aerosol actuator,
	    rectified the issue of manufacturing numerous aerosol actuators to provide the user with varied output patterns/effects. The Multi-Width Aerosol Actuator incorporates a rotatable
	    nozzle and thread design in the nozzle assembly housing. This allows the offset of the nozzle to be set, altering the width and effect off the output of the actuator, the nozzle is
	    able to be maintained by the user as the nozzle can be removed and cleaned easily and thoroughly, improving the life expectancey of the product.
	</p>
	<p>
	    With assistance from Yoobee ACG School of Design Auckland campus director, Kevin Sharp, I was able to produce a 3 Dimensional model of the Multi-Width Aerosol Actuator, and create
	    a 3D printed prototype of my innovation. With the utilisation of modern techology, a cost effective way of prototyping ideas is now widely available and I was able to produce and
	    present an actual prototype of my innovative idea, which made it easier to explain and present my project and was a nice way to wrap up six months of research and development of an idea.
	</p>
